package com.pb.news.entity.vo;

public class Query {
    private String id;
    private String goodsName;
    private String goodsCode;
    private String recPrice;
    private String retPrice;
    private String stockName;
    private String size;
    private String color;
    private String qty;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getGoodsName() {
        return goodsName;
    }

    public void setGoodsName(String goodsName) {
        this.goodsName = goodsName;
    }

    public String getGoodsCode() {
        return goodsCode;
    }

    public void setGoodsCode(String goodsCode) {
        this.goodsCode = goodsCode;
    }

    public String getRecPrice() {
        return recPrice;
    }

    public void setRecPrice(String recPrice) {
        this.recPrice = recPrice;
    }

    public String getRetPrice() {
        return retPrice;
    }

    public void setRetPrice(String retPrice) {
        this.retPrice = retPrice;
    }

    public String getStockName() {
        return stockName;
    }

    public void setStockName(String stockName) {
        this.stockName = stockName;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getQty() {
        return qty;
    }

    public void setQty(String qty) {
        this.qty = qty;
    }
}
